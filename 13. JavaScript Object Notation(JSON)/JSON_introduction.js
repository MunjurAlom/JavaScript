
/*
What is JSON?
    -> JSON- JavaScript Object Notation
    -> Data formate for exchanging data between systems running on differet techonology.
    -> Can be used with most of languages like Python, PHP etc.
        and obviously JavaScript.
    -> Has simlarity with JavaScript objects
    -> Properties are wrapped with " "
    
    -> JavaScript Object: 
            {name: "Rahim", age: 25, hometown: "Dhaka"}
    
    -> JSON Object:
            {"name": "Rahim", "age": 25, "hometown": "Dhaka"}

*/